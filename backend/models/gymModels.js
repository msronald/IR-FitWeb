import mongoose from "mongoose";
const gymSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    image: {
        type: String,
        required: true,
    },
    category: {
        type: String,
        required: true,
    },
    });

    const gymModels = mongoose.models.gym || mongoose.model("gym", gymSchema);
    export default gymModels;